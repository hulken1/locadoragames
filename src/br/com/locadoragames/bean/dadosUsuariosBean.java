package br.com.locadoragames.bean;

public class dadosUsuariosBean {

	private int idDadosClientes;
	private String nomeDadosCliente;
	private String senhaUsuario;
	private String emailDadosCliente;
	private String telefoneDadosClientes;
	
	public int getIdDadosClientes() {
		return idDadosClientes;
	}
	public void setIdDadosClientes(int idDadosClientes) {
		this.idDadosClientes = idDadosClientes;
	}
	public String getNomeDadosCliente() {
		return nomeDadosCliente;
	}
	public void setNomeDadosCliente(String nomeDadosCliente) {
		this.nomeDadosCliente = nomeDadosCliente;
	}
	
	public String getSenhaUsuario() {
		return senhaUsuario;
	}
	public void setSenhaUsuario(String senhaUsuario) {
		this.senhaUsuario = senhaUsuario;
	}
	public String getEmailDadosCliente() {
		return emailDadosCliente;
	}
	public void setEmailDadosCliente(String emailDadosCliente) {
		this.emailDadosCliente = emailDadosCliente;
	}
	public String getTelefoneDadosClientes() {
		return telefoneDadosClientes;
	}
	public void setTelefoneDadosClientes(String telefoneDadosClientes) {
		this.telefoneDadosClientes = telefoneDadosClientes;
	}
	
	
	
	
	
}
