package br.com.locadoragames.bean;

public class cadastroJogosBEAN {
		
		private int idCadastroJogo;
		private String nomeJogo;
		private double valorJogo;
		private int estoqueJogo;
		private String nomeGenero;
		private String nomeConsole;
		

		
		
		public int getIdCadastroJogo() {
			return idCadastroJogo;
		}
		public void setIdCadastroJogo(int idCadastroJogo) {
			this.idCadastroJogo = idCadastroJogo;
		}
		public String getNomeJogo() {
			return nomeJogo;
		}
		public void setNomeJogo(String nomeJogo) {
			this.nomeJogo = nomeJogo;
		}
		
	
		public double getValorJogo() {
			return valorJogo;
		}
		public void setValorJogo(double valorJogo) {
			this.valorJogo = valorJogo;
		}
		public int getEstoqueJogo() {
			return estoqueJogo;
		}
		public void setEstoqueJogo(int estoqueJogo) {
			this.estoqueJogo = estoqueJogo;
		}
		public String getNomeGenero() {
			return nomeGenero;
		}
		public void setNomeGenero(String nomeGenero) {
			this.nomeGenero = nomeGenero;
		}
		public String getNomeConsole() {
			return nomeConsole;
		}
		public void setNomeConsole(String nomeConsole) {
			this.nomeConsole = nomeConsole;
		}
		
}
	