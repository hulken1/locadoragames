package br.com.locadoragames.bean;

public class locacaoBean {

	private int idLocacao;
	private int idJogo;
	private int idCadastroClientes;
	private String dataLocacao;
	private String dataEntrega;
	private String situacao;
	public int getIdLocacao() {
		return idLocacao;
	}
	public void setIdLocacao(int idLocacao) {
		this.idLocacao = idLocacao;
	}
	public int getIdJogo() {
		return idJogo;
	}
	public void setIdJogo(int idJogo) {
		this.idJogo = idJogo;
	}
	public int getIdCadastroClientes() {
		return idCadastroClientes;
	}
	public void setIdCadastroClientes(int idCadastroClientes) {
		this.idCadastroClientes = idCadastroClientes;
	}
	public String getDataLocacao() {
		return dataLocacao;
	}
	public void setDataLocacao(String dataLocacao) {
		this.dataLocacao = dataLocacao;
	}
	public String getDataEntrega() {
		return dataEntrega;
	}
	public void setDataEntrega(String dataEntrega) {
		this.dataEntrega = dataEntrega;
	}
	public String getSituacao() {
		return situacao;
	}
	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	
	
	
	
	
}
